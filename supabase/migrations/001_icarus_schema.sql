for all
using (exists (select 1 from goals where goals.id = goal_stages.goal_id and goals.user_id = auth.uid()::text))
with check (exists (select 1 from goals where goals.id = goal_stages.goal_id and goals.user_id = auth.uid()::text));

create policy "Users manage goal tasks" on goal_tasks
for all
using (exists (select 1 from goals where goals.id = goal_tasks.goal_id and goals.user_id = auth.uid()::text))
with check (exists (select 1 from goals where goals.id = goal_tasks.goal_id and goals.user_id = auth.uid()::text));
