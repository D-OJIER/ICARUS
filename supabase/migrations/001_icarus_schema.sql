create policy "Users can read their profile" on users for select using (id = auth.uid()::text);
create policy "Users can create their profile" on users for insert with check (id = auth.uid()::text);
create policy "Users can update their profile" on users for update using (id = auth.uid()::text) with check (id = auth.uid()::text);
create policy "Users can delete their profile" on users for delete using (id = auth.uid()::text);

