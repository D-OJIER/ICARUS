create policy "Users manage earned titles" on user_earned_titles for all using (user_id = auth.uid()::text) with check (user_id = auth.uid()::text);
create policy "Users manage chronicle" on user_chronicle for all using (user_id = auth.uid()::text) with check (user_id = auth.uid()::text);
create policy "Users manage achievements" on user_achievements for all using (user_id = auth.uid()::text) with check (user_id = auth.uid()::text);
create policy "Users manage skill nodes" on user_skill_nodes for all using (user_id = auth.uid()::text) with check (user_id = auth.uid()::text);
create policy "Users manage quests" on quests for all using (user_id = auth.uid()::text) with check (user_id = auth.uid()::text);
create policy "Users manage goals" on goals for all using (user_id = auth.uid()::text) with check (user_id = auth.uid()::text);

